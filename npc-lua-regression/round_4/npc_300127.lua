function chkQState(id)
  QSTATE(id, -1)
  if qData[1411].state ~= 2 and GET_PLAYER_FACTION() == -1 then
    if qData[1411].state == 1 then
      if __QUEST_HAS_ALL_ITEMS(qt[1411].goal.getItem) then
        QSTATE(id, 2)
      else
        QSTATE(id, 1)
      end
    else
      QSTATE(id, 0)
    end
  end
end
